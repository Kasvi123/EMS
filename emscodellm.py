def tag_category(text):
    tl = text.lower()
    for cat, kws in CATEGORY_KEYWORDS.items():
        if any(k.lower() in tl for k in kws):
            return cat
    return 'other'

def clean(t):
    t = re.sub(r'\s+', ' ', t)
    t = re.sub(r'\.{2,}', '.', t)
    return t.strip()

