    print(f'Epoch {epoch:02d}/{CONFIG["epochs"]}  '
          f'Train: {tr_loss:.4f}  Val: {vl_loss:.4f}  [{marker}]')

    if patience_count >= CONFIG['patience']:
        print(f'\nEarly stopping at epoch {epoch}.')
