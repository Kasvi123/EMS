def generate_response(scenario: str, max_len: int = 300) -> str:
    input_text = CONFIG['prefix'] + scenario
    inputs = tokenizer(
        input_text,
        return_tensors='pt',
        max_length=CONFIG['max_input_len'],
        truncation=True,
    ).to(DEVICE)
    with torch.no_grad():
        out = best_model.generate(
            inputs['input_ids'],
            attention_mask=inputs['attention_mask'],
            max_length=max_len,
            num_beams=4,
            early_stopping=True,
            no_repeat_ngram_size=3,
            length_penalty=1.2,
        )
    return tokenizer.decode(out[0], skip_special_tokens=True)

